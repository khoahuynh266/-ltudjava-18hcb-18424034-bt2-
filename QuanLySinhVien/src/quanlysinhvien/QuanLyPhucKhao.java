    private void jbtnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnAddActionPerformed
        // TODO add your handling code here:
        SimpleDateFormat formatter = new SimpleDateFormat("dd/MM/yyyy");
        Date NgayBD = null;
        try {
            NgayBD = formatter.parse(jNgayBD.getText().toString());
        } catch (ParseException ex) {
            Logger.getLogger(QuanLyPhucKhao.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        Date NgayKT = null;
        try {
            NgayKT = formatter.parse(this.jNgayKT.getText());
        } catch (ParseException ex) {
            Logger.getLogger(QuanLyPhucKhao.class.getName()).log(Level.SEVERE, null, ex);
        }
        if(NgayBD!= null && NgayKT !=null){
        String NgayBd = formatter.format(NgayBD);
        String NgayKt = formatter.format(NgayKT);
        System.out.printf(NgayKt);
        if (NgayKT.after(NgayBD)) {
            String hocKy = String.valueOf(jcbHK.getItemAt(jcbHK.getSelectedIndex()));
            String namHoc = this.jNamHoc.getText();
            
                // Thêm mới
                BangPhucKhao bpk = new BangPhucKhao(hocKy, namHoc, NgayBd,NgayKt, 1);
                boolean checkCreate = BangPhucKhaoDAO.themBangPhucKhao(bpk);
                if (checkCreate) {
                    JOptionPane.showMessageDialog(null, "Tạo lịch phúc khảo thành công.", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                    initLayout();
            }
        }
        } else {
            JOptionPane.showMessageDialog(null, "Lịch không hợp lệ", "Error", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jbtnAddActionPerformed

