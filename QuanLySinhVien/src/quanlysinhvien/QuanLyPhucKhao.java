    private void initLayout() {
        List<BangPhucKhao> list = BangPhucKhaoDAO.getListBangPhucKhao();
        if (list.size() > 0) {
            int stt = 1;
            DefaultTableModel tableModel = new DefaultTableModel();
            tableModel.setColumnIdentifiers(columnNames);
            //  "STT", "ID", "From Date", "End Date", "Học Kỳ", "Năm Học", "Trạng Thái"
            for (BangPhucKhao tr : list) {
                String[] rows = new String[9];
                rows[0] = String.valueOf(stt);
                rows[1] = tr.getId()+"";
                rows[2] = tr.getNgayBatDau().toString();
                rows[3] = tr.getNgayKetThuc().toString();
                rows[4] = tr.getHocKy();
                rows[5] = tr.getNamHoc();
                if (tr.getTrangThai() == 1) {
                    rows[6] = "Đang mở";
                } else {
                    rows[6] = "Đã đóng";
                }

                tableModel.addRow(rows);
                stt++;
            }

            jTablePhucKhao.setModel(tableModel);
        } else {
            DefaultTableModel tableModel = new DefaultTableModel();
            tableModel.setColumnIdentifiers(columnNames);
            jTablePhucKhao.setModel(tableModel);
        }
    }

